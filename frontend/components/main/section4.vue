                    });
                }
            }, 100);
        };

        const scrollToPackages = () => {
            setTimeout(() => {
                const packagesSection = document.getElementById('packages');
                if (packagesSection) {
                    const offset = packagesSection.offsetTop - 100; // 150px higher than normal
                    window.scrollTo({
                        top: offset,
                        behavior: 'smooth'
                    });
                }
            }, 100);
        };

        return {
            scrollToPackages,
            scrollToFeatures
        };
    }
});
