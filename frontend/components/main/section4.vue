    name: 'Section4',
    setup() {
        onMounted(() => {
            AOS.init();
        });

        const scrollToFeatures = () => {
            setTimeout(() => {
                const featuresSection = document.getElementById('features');
                if (featuresSection) {
                    const offset = featuresSection.offsetTop - 100; // 150px higher than normal
                    window.scrollTo({
                        top: offset,
